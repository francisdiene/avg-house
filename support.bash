🖨️ 1. Installer un pilote Epson compatible

Les imprimantes SCPOS / OEM sont des clones Epson ESC/POS.

➡️ Le pilote le plus stable :
Epson TM-T88 (ou TM-T20)

📌 Même si ce n’est pas écrit Epson sur la machine, c’est normal.

Étapes :

Désinstalle l’imprimante actuelle (si possible)

Télécharge le pilote Epson TM-T88

Installe-le

Lors de l’installation :

Choisis TM-T88V ou TM-T20

Port USB ou COM selon ton branchement

⚙️ 2. Régler correctement le format papier (TRÈS IMPORTANT)

Après installation :

Panneau de configuration

Périphériques et imprimantes

Clic droit → Préférences d’impression

Vérifie :

Largeur papier : 80 mm

Pas 58 mm

Marges = 0

Densité d’impression : Normal ou Élevée

👉 Si Windows croit que c’est du 58 mm, la police devient minuscule.

🧾 3. Réglage dans le logiciel de caisse

Dans ton logiciel :

Paramètres → Impression ticket

Police : Normal / Large

Désactiver :

Small font

Condensed

Si option disponible : ESC/POS standard

🔄 4. Réinitialiser l’imprimante intégrée

Sur beaucoup de SCPOS :

Éteins la caisse

Maintiens le bouton FEED

Rallume en gardant FEED appuyé

Laisse imprimer le ticket de test

👉 Ça remet la police par défaut.

🧪 5. Test simple pour confirmer

Imprime un test Windows

Puis un ticket depuis le logiciel

Résultat attendu :

Texte lisible

Caractères plus gros

Utilisation complète des 80 mm

🎯 Résumé rapide

✔ SCPOS/OEM = compatible Epson ESC/POS
✔ Installer TM-T88 / TM-T20
